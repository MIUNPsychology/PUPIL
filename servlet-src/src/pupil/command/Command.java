public abstract class Command
{
  protected DBManager db = null;
  protected StaticInfoBlob sib = null;

  public Command(DBManager dbm, StaticInfoBlob info)
  {
    info = sib;
    db = dbm;
  }

  public abstract void process(RequestInfoBlob rib,XMLInfoBlob xib,XMLCommand cmd) throws IOException, ServletException, SAXException, ParserConfigurationException, TransformerConfigurationException, TransformerException, DOMException;
}

